public class Squadra {
    int codice;
    String sport;
    String nome_della_squadra;
    int num_partite_vinte;
    int num_partite_perse;

    public Squadra(int codice, String sport, String nome_della_squadra, int num_partite_vinte, int num_partite_perse) {
        this.codice = codice;
        this.sport = sport;
        this.nome_della_squadra = nome_della_squadra;
        this.num_partite_vinte = num_partite_vinte;
        this.num_partite_perse = num_partite_perse;
    }
    

}
