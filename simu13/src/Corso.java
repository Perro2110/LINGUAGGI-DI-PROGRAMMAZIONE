public class Corso {
    int codice;
    String tipo;
    String nomeCorso;
    String nome_cognome_docente;

    public Corso(int codice, String tipo, String nomeCorso, String nome_cognome_docente) {
        this.codice = codice;
        this.tipo = tipo;
        this.nomeCorso = nomeCorso;
        this.nome_cognome_docente = nome_cognome_docente;
    }

}
