public class Progetto {
    int codice;
    String tipo;
    String titolo_progetto;
    String nomeCognome_coordinatore;
    String orgaString;
    double importo_totale_in_milioni_di_euro;
    public Progetto(int codice, String tipo, String titolo_progetto, String nomeCognome_coordinatore, String orgaString,
            double importo_totale_in_milioni_di_euro) {
        this.codice = codice;
        this.tipo = tipo;
        this.titolo_progetto = titolo_progetto;
        this.nomeCognome_coordinatore = nomeCognome_coordinatore;
        this.orgaString = orgaString;
        this.importo_totale_in_milioni_di_euro = importo_totale_in_milioni_di_euro;
    }



}
