public class Noleggio {
    int codiceVeicolo;
    int numGiorni;
    public int getCodiceVeicolo() {
        return codiceVeicolo;
    }
    public int getNumGiorni() {
        return numGiorni;
    }
    public Noleggio(int codiceVeicolo, int numGiorni) {
        this.codiceVeicolo = codiceVeicolo;
        this.numGiorni = numGiorni;
    }
}
